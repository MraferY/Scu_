i=int(input('請輸入這個月利潤:'))
x1=100000*0.1
x2=100000*0.075+x1
x3=200000*0.05+x2
x4=200000*0.03+x3
x5=400000*0.015+x4

if(i<=100000):
    x=i*0.1
elif(i<=200000):
    x=x1+(i-100000)*0.075
elif(i<=400000):
    x=x2+(i-200000)*0.05
elif(i<=600000):
    x=x3+(i-400000)*0.03
elif(i<=1000000):
    x=x4+(i-600000)*0.015
else:
    x=x5+(i-1000000)*0.01
print('您的利潤是{}元，這個月獎金是{}'.format(i,int(x)))
